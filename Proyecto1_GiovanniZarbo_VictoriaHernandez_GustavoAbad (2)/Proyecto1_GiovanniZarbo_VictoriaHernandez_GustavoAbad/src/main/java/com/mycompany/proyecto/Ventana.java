    private void botonEliminarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarUsuarioActionPerformed
    String texto = "@" + Usuario.getText().trim();
    if (texto.equals("@")) {
        JOptionPane.showMessageDialog(this, "El campo de usuario no puede estar vacío.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    if (nuevo == null) {
        JOptionPane.showMessageDialog(this, "El grafo está vacío.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    boolean seElimino = nuevo.eliminar(texto);
    if (seElimino) {
        JOptionPane.showMessageDialog(this, "Usuario '" + texto + "' eliminado.");
    } else {
        JOptionPane.showMessageDialog(this, "El usuario '" + texto + "' no existe.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    Usuario.setText("");
    }//GEN-LAST:event_botonEliminarUsuarioActionPerformed

