    private void botonAgregarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarUsuarioActionPerformed
    String texto = "@"+ Usuario.getText().trim();
    if (texto.equals("@")) {
        JOptionPane.showMessageDialog(this, "El campo de usuario no puede estar vacío.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    if (nuevo == null) {
        nuevo = new Grafo(0);
    }
    boolean seInserto = nuevo.insertar(texto);
    if (seInserto) {
        JOptionPane.showMessageDialog(this, "Usuario '" + texto + "' agregado.");
    } else {
        JOptionPane.showMessageDialog(this, "El usuario '" + texto + "' ya existe.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    Usuario.setText("");
    }//GEN-LAST:event_botonAgregarUsuarioActionPerformed

    private void ResetearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ResetearActionPerformed
    this.nuevo = null;

    if (this.VentanaActual != null) {
        this.VentanaActual.dispose();
    }
    Usuario.setText("");
    CFC.setText("");
    JOptionPane.showMessageDialog(this, "El grafo ha sido reseteado y la memoria está limpia.");
    }//GEN-LAST:event_ResetearActionPerformed

    private void botonAgregarRelacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarRelacionActionPerformed
    String origen = "@" + Origen.getText().trim();
    String destino = "@" + Destino.getText().trim();
    if (origen.isEmpty() || destino.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debe llenar ambos campos (Origen y Destino).", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    if (origen.equals(destino)) {
        JOptionPane.showMessageDialog(this, "Un usuario no puede seguirse a sí mismo.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    if (nuevo == null) {
        JOptionPane.showMessageDialog(this, "Aún no se ha cargado ningún grafo.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    boolean seAgrego = nuevo.agregarArista(origen, destino);
    if (seAgrego) {
        JOptionPane.showMessageDialog(this, "Relación '" + origen + " -> " + destino + "' agregada.");
    } else {
        JOptionPane.showMessageDialog(this, "No se pudo agregar la relación. Verifique que:\n1. Los usuarios existan.\n2. La relación no sea un duplicado.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    Origen.setText("");
    Destino.setText("");
    }//GEN-LAST:event_botonAgregarRelacionActionPerformed

